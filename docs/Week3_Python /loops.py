# Loops
# There two types of loops in python, for and while
# Loops iterate over a given sequence
# Loops can iterate through lists, strings, dictionaries and tuples

list_data = [1, 2, 3]

# Prints 1, 2, 3
for n in list_data:
    print(n)

# Prints out the numbers 0, 1, 2, 3, 4
for x in range(5):
    print(x)

